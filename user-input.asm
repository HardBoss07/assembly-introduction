DATA SEGMENT USE16
MESG DB 'Enter your Name:', '$'
BUF DB 20, 0, 20 DUP('$')
MSG DB 'Your Name is: ', '$'
DATA ENDS

CODE SEGMENT USE16
    ASSUME CS:CODE, DS:DATA

BEG:
    MOV AX, DATA
    MOV DS, AX

    MOV AH, 9
    MOV DX, OFFSET MESG
    INT 21H

    MOV AH, 0AH
    MOV DX, OFFSET BUF
    INT 21H

    LEA SI, BUF+2
    MOV CX, 20

CHECK_CR:
    LODSB
    CMP AL, 13
    JE ADD_TERMINATOR
    LOOP CHECK_CR
    JMP DISPLAY

ADD_TERMINATOR:
    DEC SI
    MOV BYTE PTR [SI], '$'

DISPLAY:
    MOV AH, 9
    MOV DX, OFFSET MSG
    INT 21H

    MOV AH, 9
    MOV DX, OFFSET BUF+2
    INT 21H

    MOV AH, 4CH
    INT 21H


CODE ENDS
END BEG
